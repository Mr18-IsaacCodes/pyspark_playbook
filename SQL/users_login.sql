-- Which users have a difference of 5 or more months between their first and last login timestamps? 
select USER_ID,month(current_timestamp())-month(mx) as diff
from(
select USER_ID,min(LOGIN_TIMESTAMP) as mn,max(LOGIN_TIMESTAMP) as mx from logins
group by USER_ID)a
having diff>=5;

-- How many sessions and unique users logged in during each quarter, and what was the quarter of the earliest login timestamp?
select quarter(min(login_timestamp)),
quarter(LOGIN_TIMESTAMP),count(*) as session_cnt,count(distinct USER_ID) as usr_cnt
from logins
group by quarter(LOGIN_TIMESTAMP);

-- For each quarter, what is the start date of the quarter, the session count, and the unique user count?
SELECT
  DATE_FORMAT(DATE_ADD(DATE_SUB(login_timestamp, INTERVAL DAYOFMONTH(login_timestamp) - 1 DAY), INTERVAL - MONTH(login_timestamp) % 3 MONTH), '%Y-%m-%d') AS quarter_start,
  QUARTER(login_timestamp) AS quarter,
  COUNT(*) AS session_cnt,
  COUNT(DISTINCT USER_ID) AS usr_cnt
FROM
  logins
GROUP BY
  quarter(login_timestamp)
ORDER BY
  quarter(login_timestamp);
